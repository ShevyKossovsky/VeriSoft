package VinylRecordStore;

/**
 * The RecordPlayer interface represents a traditional vinyl record player.
 * It provides methods to play and pause a vinyl record.
 */
public interface RecordPlayer {

    /**
     * Starts playing the vinyl record.
     * This method simulates the action of starting the playback of a record.
     */
    void play();

    /**
     * Pauses the vinyl record playback.
     * This method simulates the action of pausing a record that is currently playing.
     */
    void pause();
}
