package FashionDesignStudio.Models;

/**
 * Abstract class representing the top part of an outfit.
 * This class serves as a blueprint for creating specific types of tops,
 * such as casual or formal tops.
 */
public abstract class Tops {

    /**
     * Abstract method that defines the behavior of enjoying the top.
     * This method should be implemented by concrete classes to specify how
     * the top can be enjoyed or used.
     */
    public abstract void enjoy();
}
