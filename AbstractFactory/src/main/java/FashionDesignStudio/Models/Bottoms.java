package FashionDesignStudio.Models;

/**
 * Abstract class representing the bottom part of an outfit.
 * This class serves as a blueprint for creating specific types of bottoms,
 * such as casual or formal bottoms.
 */
public abstract class Bottoms {

    /**
     * Abstract method that defines the behavior of enjoying the bottom.
     * This method should be implemented by concrete classes to specify how
     * the bottom can be enjoyed or used.
     */
    public abstract void enjoy();
}
